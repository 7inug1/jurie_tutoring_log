function getProvinces() {
  navigationLists.forEach((navigationList, index) => {
    navigation.appendChild(getProvinceButtonsList(navigationList, index));
    navigationWrapper.appendChild(navigation);
    navigationWrapper.appendChild(subnavigation);

    getRegions(navigationList);
  });
}

function getProvinceButtonsList(navigationList, index) {
  let navigationLI = document.createElement('li');
  navigationLI.id = index;
  navigationLI.appendChild(getProvinceButtons(navigationList, index));

  return navigationLI;
}

function getProvinceButtons(navigationList, index) {
  console.log(navigationList);
  let button_head = document.createElement('button');
  let button_tail = document.createElement('span');
  let button_body = document.createElement('span');

  button_head.className = 'button00';
  button_head.type = 'button';
  button_tail.className = 'tail';
  button_body.className = 'body';
  button_body.innerText = navigationList.title;
  if (navigationList.title !== '전국') button_body.appendChild(getPensionNumberSpan(index));
  button_tail.appendChild(button_body);
  button_head.appendChild(button_tail);
  return button_head;
}
function getPensionNumberSpan(index) {
  let numberSpan = document.createElement('span');
  numberSpan.className = 'number';
  numberSpan.innerText = getPensionNumbers(index);
  return numberSpan;
}

function getPensionNumbers(index) {
  let number = 0;
  navigationLists[index].list.forEach((item) => {
    number += item.list.length;
  });
  return number;
}

function getRegions(navigationList) {
  navigationList.list.forEach((subnavigationList) => {
    let regionLI = document.createElement('li');
    let pensionUL = document.createElement('ul');

    getPensions(pensionUL, regionLI, subnavigationList);

    regionLI.appendChild(getRegionButton(subnavigationList));
    subnavigation.appendChild(regionLI);
    regionLI.appendChild(pensionUL);
  });
}

// region
function getRegionButton(subnavigationList) {
  let regionButton = document.createElement('button');
  regionButton.type = 'button';
  regionButton.innerText = `${subnavigationList.title}`;
  return regionButton;
}

// sub
function getPensions(pensionUL, regionLI, subnavigationList) {
  subnavigationList.list.forEach((pension) => {
    pensionUL.appendChild(getFunctionList(pension));
    regionLI.appendChild(pensionUL);
  });
}

function getFunctionList(pension) {
  let pensionLI = document.createElement('li');
  pensionLI.appendChild(getPensionLink(pension));
  return pensionLI;
}

function getPensionLink(pension) {
  let anchor = document.createElement('a');
  anchor.appendChild(getPensionTitle(pension));
  anchor.appendChild(getPopulatedIconContainer(pension));
  return anchor;
}

function getPensionTitle(pension) {
  let textSpan = document.createElement('span');
  textSpan.innerText = pension.title;
  return textSpan;
}

// .icon-container
function getPopulatedIconContainer(pension) {
  let iconContainerSpan = document.createElement('span');
  iconContainerSpan.className = 'icon-container';

  if (pension.renewal && pension.new) {
    iconContainerSpan.appendChild(getIconRenewal());
    iconContainerSpan.appendChild(getIconNew());
  } else if (pension.renewal && !pension.new) {
    iconContainerSpan.appendChild(getIconRenewal());
  } else if (!pension.renewal && pension.new) {
    iconContainerSpan.appendChild(getIconNew());
  }
  return iconContainerSpan;
}

function getIconRenewal() {
  let iconR = document.createElement('img');
  iconR.src = '../images/header/navigation/subnavigation/icon_R.png';
  return iconR;
}

function getIconNew() {
  let iconN = document.createElement('img');
  iconN.src = '../images/header/navigation/subnavigation/icon_N.png';
  return iconN;
}

